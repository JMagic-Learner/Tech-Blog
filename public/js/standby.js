const login = async () => {
    const response = await fetch('homepage', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
    });
  
    if (response.ok) {
      document.location.replace('/login');
    } else {
      alert(response.statusText);
    }
  };

document.getElementById('login').addEventListener('click', login);